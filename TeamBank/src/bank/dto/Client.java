package bank.dto;

public class Client {	
	private String cltCode, cltPass, cltName, cltGrade;

	public String getCltCode() {
		return cltCode;
	}

	public void setCltCode(String cltCode) {
		this.cltCode = cltCode;
	}

	public String getCltPass() {
		return cltPass;
	}

	public void setCltPass(String cltPass) {
		this.cltPass = cltPass;
	}

	public String getCltName() {
		return cltName;
	}

	public void setCltName(String cltName) {
		this.cltName = cltName;
	}

	public String getCltGrade() {
		return cltGrade;
	}

	public void setCltGrade(String cltGrade) {
		this.cltGrade = cltGrade;
	}
	
	

}
